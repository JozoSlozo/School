//kvetina tulipan, delka, sirka
int kvetina1(int delka, int sirka){
  int d_stonek = (int)(delka / 2);//delka stonku
  int s_stonek = sirka / 3;// sirka stonku
  int start_stonku = (sirka - s_stonek) / 2;//pixel kde stonek zacina
  int d_kvetu = delka - d_stonek;//delka kvetu
  int kraj_kvetu = sirka - ((int)(sirka / 2));//velikosti okraju vrchni casti kvetu
  for (int i = 0; i < delka; i++)
  {
    for (int a = 0; a < sirka; a++)
    {
      if(i < d_kvetu && !(i < d_kvetu - ((int)(d_kvetu / 2)) && a >= kraj_kvetu / 2 && a < sirka - kraj_kvetu / 2)){//kvet
        set_red_color();
        draw_pixel();
      }
      else if(i >= d_stonek && a >= start_stonku && a < start_stonku + s_stonek){//stonek
        set_green_color();
        draw_pixel();
      }
      move_right();
    }
    move_down();
    for (int a = 0; a < sirka; a++){//vratit zpet na zacatek
      move_left();
    }
  }
  return 0;
}

//kvetina slunecnice, delka, sirka
int kvetina2(int delka, int sirka){
  int d_stonek = (int)(delka / 2);//delka stonku
  int s_stonek = sirka / 3;//sirka stonku
  int start_stonku = (sirka - s_stonek) / 2;//kde zacina stonek
  int d_kvetu = delka - d_stonek;//delka kvetu
  for (int i = 0; i < delka; i++)
  {
    for (int a = 0; a < sirka; a++)
    {
      if(i < d_kvetu){//kvet
        set_yellow_color();
        if(a >= start_stonku && a < start_stonku + s_stonek &&  i > (d_kvetu / 3)-1 && i < 2 * (d_kvetu / 3)){//pokud prostředek
          set_black_color();
        }
        draw_pixel();
      }
      else if(i >= d_stonek && a >= start_stonku && a < start_stonku + s_stonek){//stonek
        set_green_color();
        draw_pixel();
      }
      move_right();
    }
    move_down();
    for (int a = 0; a < sirka; a++){//cratit zpatky
      move_left();
    }
  }
  return 0;
}

//louka vykresleni nahodnych kvetin, sloupce, radky, vyska kvetin, sirka kvetin
int louka(int sloupce, int radky, int vyska_kve, int sirka_kve){
  int (*kvetiny[])(int, int) = {kvetina1, kvetina2};
  int mezera_vyska = vyska_kve+2;
  int mezera_sirka = sirka_kve+2;
  for (int i = 0; i < sloupce; i++)
  {
    for (int a = 0; a < radky; a++)
    {
      int r = rand() % 2;
      move_to((i * mezera_vyska)+1, (a * mezera_sirka)+1);
      (*kvetiny[r])(vyska_kve,sirka_kve);
    }
  }
  return 0;
}

//vykresli 2d list cisel kde 0 je mezera a 1...5 jsou barvy, vyska obrazce, sirka obrazce, list cisel
int vykresli_map(int vyska, int sirka, int map[vyska][sirka]){
  for(int l = 0; l < vyska; l++){
    for (int c = 0; c < sirka; c++)
    {
      if(map[l][c] != 0){
        barvy[map[l][c]-1]();
        move_right();
        draw_pixel();
        move_right();
        draw_pixel();
      }
      else{
        move_right();
        move_right();
      }
    }
    move_to(l+2, 1);
  }
  return 0;
}

